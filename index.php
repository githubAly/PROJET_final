                <div class="lets">
                    <div class="para" style="display:block">
                        <h3 style="text-transform:uppercase; font-size: 22px; padding-top: 20px;"> Débuts </h3>
                        <p>Si aujourd’hui, il est bien connu du public français, Vald le doit, en partie, à ses mixtapes de début de carrière. En 2012, il officialise la sortie de deux mixtapes : NQNTMQMQMB (Ni Queue Ni Tête Mais Qui Met Quand Même Bien)
                            et Cours de rattrapage.<br> <br> Proposées en accès libre et gratuit sur son site, elles seront ensuite rééditées en 2016 en double CD et mises en vente sur le même site. En 2014, Vald va prendre une grande propension grâce,
                            notamment, à la sortie de son tout premier EP NQNT (Ni Queue Ni Tête). <br> <br>L’année suivante, il réalise et publie le clip de son morceau Bonjour qui révèle au grand public l’attrait du rappeur pour l’absurde. Ce clip est
                            visualisé plus d’un million de fois. Vald enchaine avec la publication de son second EP NQNT 2 le 25 septembre 2015, qui contient ledit morceau. NQNT 2 sort 1er dans la catégorie des ventes numériques hebdomadaires et 11e tous
                            supports confondus en France. </p>

