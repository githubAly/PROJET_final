                <div id="gauche">
                    <div style="display:block">
                        <h3 style="text-transform:uppercase; font-size: 22px; padding-top: 20px; text-align:start;"> Inspirations </h3>
                        <p>À travers ses textes et ses interviews, Vald se dit très inspiré, amusé, de théories du complot, comme celle des reptiles dont il parle notamment dans le titre Barème de NQNT 2 mais aussi dans Écailles de l'album de DJ Weedim Boulangerie
                            Française et dans le titre Lezarman, de l'album Agartha. Le rappeur précise ne pas y croire personnellement, tout en appréciant le fait qu'elles soient autant de possibilités. Par ailleurs, il se dit convaincu que les êtres
                            humains sont contrôlés par une puissance suprême, « moi-même Valentin, je n'accepte pas cette réalité, sans un esprit malsain, qui organise toute cette merde. Je peux pas comprendre que l'humain est tellement une merde qu'il
                            se fout lui-même dans la merde sans faire exprès, j'peux pas voir le monde comme ça […] et selon moi ce sont les reptiles ». <br> <br> Vald est aussi fortement inspiré par les films d'horreur. Il apprécie l'ambiance sombre
                            et lugubre que ceux-ci dégagent. Il s'inspire également des films pornographiques auxquels il fait régulièrement référence dans ses textes.
