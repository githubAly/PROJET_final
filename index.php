                    <h3 style="text-transform:uppercase; font-size: 22px; padding-top: 20px; text-align:start;"> Enfance </h3>
                    <p>Né francilien d'un père breton, Vald grandit à Aulnay-sous-Bois. Jusqu'à l'âge de 12 ans, il vit avec ses parents dans un pavillon2. Il a une sœur et trois frères.
                        <br><br> Scolarisé au collège et lycée privé catholique l'Espérance, il apprécie les mathématiques et obtient un baccalauréat scientifique3. Il effectue un semestre d'études de médecine, puis arrête pour obtenir une licence mathématiques-informatique
                        et étudier dans une école d'ingénieur du son. <br><br> Avec son meilleur ami AD, ils écrivent et filment des sketches qu'ils postent sur YouTube ; ils se produisent à Paris sur des scènes ouvertes mais sans rencontrer le succès.
                        Vald découvre le rap vers l'âge de 15 ans en écoutant des titres de Kery James sur internet. Il commence à rapper à l'âge de 17 ans3. Il affirme ne pas posséder une forte culture du hip-hop due à sa découverte tardive. Il travaille
                        pendant l'été pour s'acheter du matériel</p>
