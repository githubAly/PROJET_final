            <h2 style="text-align: center; font-size: 50px; padding-top: 50px"> Royaume du V</h2>
            <div class="lets">
                <div class="para" style="display:block">
                    <h3 style="text-transform:uppercase; font-size: 22px; padding-top: 20px;"> Généralités </h3>
                    <p>Vald, de son vrai nom Valentin Le Du, né le 15 juillet 1992 à Aulnay-sous-Bois en Seine-Saint-Denis, est un rappeur, producteur et parolier français. Remarqué pour ses mixtapes et EP diffusés sur Internet, sa notoriété grandit au milieu
                        des années 2010 avec des clips au style tantôt désinvolte, tantôt outrancier, mêlant l'humour et l'absurde voire le burlesque. </p>
                    <p>Ses deux premiers albums studio, Agartha et XEU, sortis en 2017 et 2018, sont salués par la critique et certifiés double disque de platine. Son single Désaccordé est le titre le plus écouté de l'année 2018 en France. </p>
                    <p>Critique de la société, notamment sur le rôle du capitalisme et des médias, son troisième album intitulé Ce monde est cruel, publié en 2019, est analysé comme plus sérieux et politique que les précédents albums. </p>
                    <p>Après s'être produit devant un Palais omnisports de Paris-Bercy plein, sa tournée 2020 est annulée à cause de la pandémie de Covid-19. Vald crée son label indépendant, Echelon Record, en 2020. <br> La sortie d'un album commun avec
                        Heuss l'Enfoiré intitulé Horizon Vertical puis du single Footballeur sont considérés comme des échecs. </p>
                    <p> Son quatrième album, V, disque d'or en une semaine, reçoit de nombreux éloges.</p>
